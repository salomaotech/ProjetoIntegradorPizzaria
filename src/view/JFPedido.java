    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        // add pedido
        addPedido();

        // trata dados
        TrataDados trata = new TrataDados();

        // trata dado e converte em moeda
        String total = String.valueOf(pedido.getTotalPagar());
        total = trata.formataMoeda(total);

        // seta o total
        this.JLTotal.setText(total);

    }//GEN-LAST:event_jButton3ActionPerformed

