    // cria a tabela do pedido
    private void criaTabela() {

        BancoDados banco = new BancoDados();
        Map colunas = new LinkedHashMap();
        colunas.put("cliente_nome", "varchar(256)");
        colunas.put("cliente_cpf", "varchar(20)");
        colunas.put("cliente_telefone", "varchar(20)");
        colunas.put("cliente_endereco", "varchar(256)");
        colunas.put("lista_produtos", "text");
        colunas.put("total", "varchar(10)");
        colunas.put("pagamento_realizado", "varchar(3)");
        colunas.put("entrega", "varchar(3)");
        colunas.put("comanda", "varchar(3)");
        colunas.put("comanda_mesa", "varchar(128)");
        colunas.put("comanda_funcionario", "varchar(256)");
        colunas.put("data", "varchar(12)");
        banco.criarTabela(colunas, "tb_pedido");

    }

    // carrega os itens
    private void carregaItens() {

        JListDados lista = new JListDados("tb_produto", "nome");
        listaDados = lista.recuperaDados(JLCardapio);

    }

    // pesquisa itens
    private void pesquisarItens() {

        // valores de pesquisa
        Map colunasPesquisa = new LinkedHashMap();
        colunasPesquisa.put("nome", this.JTNome.getText());

        // pesquisa
        JListDados lista = new JListDados("tb_produto", "nome");
        listaDados = lista.pesquisaDados(JLCardapio, colunasPesquisa);

    }

    // exibe a imagem do produto
    private void exibeImagemProduto() {

        // trata excessao
        try {

            // valida o tamanho dos dados
            if (listaDados.size() > 0) {

                // banco de dados
                BancoDados banco = new BancoDados();

                // carrega a imagem
                CarregaImagem imagem = new CarregaImagem(banco.getURLSERVIDOR() + "/imagens/" + listaDados.get(this.JLCardapio.getSelectedIndex()) + ".jpg", this.JLImagem.getHeight(), this.JLImagem.getWidth());

                // seta a imagem
                this.JLImagem.setText("");
                this.JLImagem.setIcon(imagem.getImagem());

            }

        } catch (java.lang.ArrayIndexOutOfBoundsException ex) {

        }

    }

    // adiciona ao pedido
    private void addPedido() {

        // trata excessão
        try {

            // index do produto
            String idProduto = (String) listaDados.get(this.JLCardapio.getSelectedIndex());

            // adiciona o produto selecionado
            pedido.addProduto(idProduto, 1);

        } catch (java.lang.ArrayIndexOutOfBoundsException ex) {

            JOptionPane.showMessageDialog(null, "Selecione um ítem do menu.");

        }

    }

