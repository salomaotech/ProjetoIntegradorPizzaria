    private void JBFinalizaPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBFinalizaPedidoActionPerformed

        // valida a quantidade de itens
        if (pedido.getQuantidadeItens() == 0) {

            Algoritmos.dialogo("Não há itens!");

        } else {

            // trata dado e converte em moeda
            String total = String.valueOf(pedido.getTotalPagar());

            // modal para confirmar compra
            JDFinalizaPedido janela = new JDFinalizaPedido(null, true);
            janela.setTotal(total);
            janela.setCompra(pedido);
            janela.setJanela(this);
            janela.setVisible(true);

        }


    }//GEN-LAST:event_JBFinalizaPedidoActionPerformed

