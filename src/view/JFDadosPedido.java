    // carrega os dados pelo id
    private void carregaDados() {

        // id
        id = VariaveisTemporarias.getID(true);

        // valida se o id não é nulo
        if (isNull(id) == false) {

            // recupera os dados do banco
            BancoDados banco = new BancoDados();
            Map dados = banco.recuperaDadosChavePrimaria("id", id, "tb_pedido");

            // adiciona os dados do cliente
            DefaultListModel listModelCliente = new DefaultListModel();
            listModelCliente.addElement("Nome: " + dados.get("cliente_nome"));
            listModelCliente.addElement("CPF: " + dados.get("cliente_cpf"));
            listModelCliente.addElement("Telefone: " + dados.get("cliente_telefone"));
            listModelCliente.addElement("Endereço: " + dados.get("cliente_endereco"));
            this.JLCliente.setModel(listModelCliente);

            // dados do pedido
            String pedido = String.valueOf(dados.get("lista_produtos"));

            // lista de itens
            List<String> itens = Arrays.asList(pedido.split("\n"));

            // list model de produtos
            DefaultListModel listModelProdutos = new DefaultListModel();

            // percorre os itens da lista
            for (String item : itens) {

                // lista de itens
                List<String> subItens = Arrays.asList(item.split(" >> "));

                // adiciona linhas
                listModelProdutos.addElement("______________________________________________________________");
                listModelProdutos.addElement("Código: " + subItens.get(0));
                listModelProdutos.addElement("Produto: " + subItens.get(1));
                listModelProdutos.addElement("Quantidade: " + subItens.get(3));
                listModelProdutos.addElement("Total R$: " + subItens.get(2));
                listModelProdutos.addElement("______________________________________________________________");

            }

            // adiciona os produtos
            this.JLPedido.setModel(listModelProdutos);

            // valida se o pagamento já foi realizado
            if (dados.get("pagamento_realizado").equals("Sim") == true) {

                JBFinalizaPedido.setEnabled(false);

            }

        }

    }

