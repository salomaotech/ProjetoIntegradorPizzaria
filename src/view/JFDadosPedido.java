    private void JBFinalizaPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBFinalizaPedidoActionPerformed

        // confirma
        if (JOptionPane.showConfirmDialog(null, "Deseja finalizar o pedido e lançar no caixa?") == 0) {

            // query
            String query = "update tb_pedido set pagamento_realizado='Sim' where id='" + id + "';";

            // novo banco
            BancoDados banco = new BancoDados();

            // novo mysql
            Mysql mysql = new Mysql(banco.getServidor(), banco.getUsuario(), banco.getSenha(), banco.getNomeBanco());

            // executa
            mysql.executaQuery(query);

            // desconecta
            mysql.desconecta();

            // fecha esta janela         
            this.dispose();

        }

    }//GEN-LAST:event_JBFinalizaPedidoActionPerformed

