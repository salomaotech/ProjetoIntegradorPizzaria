    // carrega os itens
    private void carregaItens() {

        List colunas = new ArrayList();
        colunas.add("nome");
        colunas.add("cpf");
        colunas.add("telefone");
        JTableDados dados = new JTableDados("tb_cliente", colunas);
        dados.recuperaDados(JTableResultados, new JFCliente());

    }

    // pesquisa itens
    private void pesquisarItens() {

        // colunas
        List colunas = new ArrayList();
        colunas.add("nome");
        colunas.add("cpf");
        colunas.add("telefone");

        // valores de pesquisa
        Map colunasPesquisa = new LinkedHashMap();
        colunasPesquisa.put("nome", this.JTNome.getText());
        colunasPesquisa.put("cpf", this.JTCpf.getText());

        // pesquisa pelos dados
        JTableDados dados = new JTableDados("tb_cliente", colunas);
        dados.pesquisaDados(JTableResultados, new JFCliente(), colunasPesquisa, true, null);

    }

